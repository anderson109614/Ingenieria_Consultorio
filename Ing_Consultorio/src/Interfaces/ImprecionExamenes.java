private void limpiadoTabla() {
        int fil = modelo.getRowCount() - 1;
        //  Limpiado del jTable de paciente
        if (fil > 0) {
            for (int j = fil; j >= 0; j--) {
                modelo.removeRow(modelo.getRowCount() - 1);
                
            }
        }
    }
