    private String exaSelect() throws NumberFormatException {
        // TODO add your handling code here:
        String cod;
        TableModel model = jTable1.getModel();
        int a = jTable1.getSelectedRow();
        if (a == -1) {
          cod="-1";
        } else {

            cod=model.getValueAt(a, 0).toString();
        }
        return cod;
    }
    private void BusLlenadoConsultas() throws HeadlessException {
        // TODO add your handling code here:
        ResultSet resCon= con.BusConsultasCli(jTextFieldCI.getText());
        
        Object[] datos= new Object[6];
        try {
            while(resCon.next()){
                String nom=resCon.getString(1)+" "+resCon.getString(2);
                jLabelNomCli.setText(nom);
                datos[0]=resCon.getString(3);
                datos[1]=resCon.getString(4);
                datos[2]=resCon.getString(5);
                datos[3]= con.contExamenesTOT((String)datos[0]);
                datos[4]= con.contExamenesTER((String)datos[0]);
                datos[5]= con.totalCOn((String)datos[0]);
                modelo.addRow(datos);
                
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            jLabelNomCli.setText("");
        }
    }

