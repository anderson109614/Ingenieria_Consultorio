    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(validarNomPre()){
          String codTiP= con.codigoTipoExa(jComboBox1.getSelectedItem().toString());
          String[] datos= new String[4];
          datos[0]=jTextFieldCodigo.getText();
          datos[1]=jTextFieldNombre.getText();
          datos[2]=jTextFieldCOsto.getText();
          datos[3]=codTiP;
          if(con.insertarExamen(datos)){
            JOptionPane.showMessageDialog(this,"Examen registrado correctamente");
          }else{
            JOptionPane.showMessageDialog(this,"Fallo en el registro del examen");  
          }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

