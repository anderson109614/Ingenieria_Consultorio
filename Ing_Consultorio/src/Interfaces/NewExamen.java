    public boolean validarNomPre(){
        if(jTextFieldNombre.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Ingrese un nombre de examen.....!!");
            return false;
        }
        if(jTextFieldCOsto.getText().equals("")){
             JOptionPane.showMessageDialog(this, "Ingrese un costo de examen.....!!");
            return false;
        }else{
             	try {
		Integer.parseInt(jTextFieldCOsto.getText());
		return true;
	        } catch (NumberFormatException nfe){
                    JOptionPane.showMessageDialog(this, "Ingrese un costo de examen valido.....!!");
		return false;
	}
        }
        
        
    }
